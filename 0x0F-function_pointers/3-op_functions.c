#include "3-calc.h"

/**
 * op_add - addittion
 * @a: a
 * @b: b
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: a
 * @b: b
 * Return: int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: a
 * @b: b
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: a
 * @b: b
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: a
 * @b: b
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
